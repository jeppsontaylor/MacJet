#!/usr/bin/env python3
"""
Emit a copy-paste README / docs fragment from committed benchmark_compare JSON files.

Usage (from repo root):
  ./scripts/benchmark_readme_snippet.py
  ./scripts/benchmark_readme_snippet.py benchmarks/results/*.json benchmark_compare_*.json

Requires Python 3.9+ (stdlib only).
"""

from __future__ import annotations

import argparse
import glob
import json
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


def _finished_at(data: dict[str, Any], path: Path) -> str:
    run = data.get("run") or {}
    s = run.get("finished_at_utc")
    if isinstance(s, str) and s:
        return s
    try:
        ts = path.stat().st_mtime
        return datetime.fromtimestamp(ts, tz=timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    except OSError:
        return "?"


def _chip(data: dict[str, Any]) -> str:
    mac = (data.get("system") or {}).get("macos") or {}
    return str(mac.get("chip_type") or mac.get("machine_model") or "—")


def _machine(data: dict[str, Any]) -> str:
    mac = (data.get("system") or {}).get("macos") or {}
    parts = [
        mac.get("machine_name"),
        mac.get("machine_model"),
    ]
    return " ".join(str(p) for p in parts if p) or "—"


def _ram(data: dict[str, Any]) -> str:
    sys = data.get("system") or {}
    mac = sys.get("macos") or {}
    if mac.get("physical_memory"):
        return str(mac["physical_memory"])
    b = (sys.get("sysinfo") or {}).get("total_memory_bytes")
    if isinstance(b, int) and b > 0:
        return f"{b / (1024**3):.1f} GiB (sysinfo)"
    return "—"


def _fmt_pct(x: Any) -> str:
    if isinstance(x, (int, float)):
        return f"{x:.2f}%"
    return "—"


def _row(path: Path, data: dict[str, Any]) -> list[str]:
    summ = data.get("summary") or {}
    return [
        path.name,
        _finished_at(data, path),
        _machine(data),
        _chip(data),
        _ram(data),
        _fmt_pct(summ.get("macjet_cpu_avg")),
        _fmt_pct(summ.get("activity_monitor_cpu_avg")),
    ]


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "files",
        nargs="*",
        default=[],
        help="JSON files (default: benchmarks/results/*.json and ./benchmark_compare_*.json)",
    )
    args = ap.parse_args()

    repo = Path.cwd()
    paths: list[Path] = []
    if args.files:
        for p in args.files:
            if any(c in p for c in "*?[]"):
                paths.extend(Path(x) for x in sorted(glob.glob(p)))
            else:
                paths.append(Path(p))
    else:
        paths.extend(sorted(repo.glob("benchmarks/results/benchmark_compare_*.json")))
        paths.extend(sorted(repo.glob("benchmark_compare_*.json")))

    rows: list[tuple[str, Path, dict[str, Any]]] = []
    for path in paths:
        if not path.is_file():
            continue
        try:
            raw = path.read_text(encoding="utf-8")
            data = json.loads(raw)
        except (OSError, json.JSONDecodeError) as e:
            print(f"# skip {path}: {e}", file=sys.stderr)
            continue
        if not isinstance(data, dict) or "summary" not in data:
            print(f"# skip {path}: not a benchmark_compare summary", file=sys.stderr)
            continue
        key = _finished_at(data, path)
        rows.append((key, path, data))

    rows.sort(key=lambda t: t[0])

    headers = [
        "File",
        "Finished (UTC)",
        "Machine",
        "Chip / model",
        "RAM",
        "MacJet avg CPU",
        "AM avg CPU",
    ]
    table_rows = [_row(p, d) for _, p, d in rows]

    def col_widths() -> list[int]:
        w = [len(h) for h in headers]
        for r in table_rows:
            for i, cell in enumerate(r):
                w[i] = max(w[i], len(cell))
        return w

    w = col_widths()

    lines: list[str] = [
        "<!-- BEGIN BENCHMARK_README_SNIPPET (generated by scripts/benchmark_readme_snippet.py) -->",
        "",
        "Paste the block below into README or docs; re-run the script after new `benchmarks/results/*.json` commits.",
        "",
    ]

    def fmt_row(cells: list[str]) -> str:
        return "| " + " | ".join(c.ljust(w[i]) for i, c in enumerate(cells)) + " |"

    sep = "|" + "|".join("-" * (x + 2) for x in w) + "|"
    lines.append(fmt_row(headers))
    lines.append(sep)
    for r in table_rows:
        lines.append(fmt_row(r))
    lines.append("")
    lines.append("<!-- END BENCHMARK_README_SNIPPET -->")

    print("\n".join(lines))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
